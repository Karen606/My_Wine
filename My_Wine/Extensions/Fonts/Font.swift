import UIKit


extension UIFont {
    static func robotoRegular(size: CFloat) -> UIFont? {
        return UIFont(name: "Roboto-Regular", size: CGFloat(size))
    }
    
    static func jostLight(size: CFloat) -> UIFont? {
        return UIFont(name: "Jost-Light", size: CGFloat(size))
    }
    
    static func jostRegular(size: CFloat) -> UIFont? {
        return UIFont(name: "Jost-Regular", size: CGFloat(size))
    }
    
    static func jostMedium(size: CFloat) -> UIFont? {
        return UIFont(name: "Jost-Medium", size: CGFloat(size))
    }
    
    static func robotoMedium(size: CFloat) -> UIFont? {
        return UIFont(name: "Roboto-Medium", size: CGFloat(size))
    }
    
    static func interRegular(size: CFloat) -> UIFont? {
        return UIFont(name: "Inter-Regular", size: CGFloat(size))
    }
    
}
